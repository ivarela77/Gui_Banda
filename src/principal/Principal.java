    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Connection miConexion=Utilidades.conexionBD();
        
        if(tablaMusicos.getSelectedRow()!=-1){
            int id=Integer.parseInt(txtIdEliminar.getText());
            int dialogo=JOptionPane.showConfirmDialog(this, "¿Realmente desea eliminar este Músico?", "Comfirmación", JOptionPane.YES_NO_OPTION);
            if(dialogo==0){
                Utilidades.eliminarMusico(miConexion, id);

                DefaultTableModel modelo=new DefaultTableModel();
                Utilidades.resultadoEnTabla(miConexion, modelo);
                tablaMusicos.setModel(modelo);
                txtIdEliminar.setText("");
                txtNombreEliminar.setText("");
                txtApellidosEliminar.setText("");
                txtDireccionEliminar.setText("");
            }
        } else{
            JOptionPane.showMessageDialog(this, "Selecciona el Músico a borrar primero!!!", "Información", JOptionPane.INFORMATION_MESSAGE);
        } 
    }//GEN-LAST:event_btnEliminarActionPerformed

