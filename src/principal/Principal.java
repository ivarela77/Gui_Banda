    private void btnAniadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAniadirActionPerformed
        Connection miConexion=Utilidades.conexionBD();
        int id=0;
        boolean cid=true;
        try{
            id=Integer.parseInt(txtIdNuevo.getText());
            cid=Utilidades.comprobarID(miConexion, id);
            if(cid==false){
                JOptionPane.showMessageDialog(null, "El ID ingresado ya está en uso\nIngresa un ID válido", "ERROR", JOptionPane.INFORMATION_MESSAGE);
            }
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(this, "Id del Músico incorrecto!!!", "Información", JOptionPane.INFORMATION_MESSAGE);
        }
        
        String nombre=txtNombreNuevo.getText();
        String apell=txtApellidosNuevo.getText();
        String direc=txtDireccionNuevo.getText();
        if(!(nombre.equals(""))&&!(apell.equals(""))&&!(direc.equals(""))&&(cid==true)){
            Utilidades.aniadir(miConexion,id,nombre,apell,direc);

            DefaultTableModel modelo=new DefaultTableModel();
            Utilidades.resultadoEnTabla(miConexion, modelo);
            tablaMusicos.setModel(modelo);

            txtIdNuevo.setText("");
            txtNombreNuevo.setText("");
            txtApellidosNuevo.setText("");
            txtDireccionNuevo.setText("");
            txtDireccionNuevo.setText("");
        }else if(cid==true){
            JOptionPane.showMessageDialog(this, "Todos los datos son obligatorios\n Revisa los datos!!!", "Información", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAniadirActionPerformed

